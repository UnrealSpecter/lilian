<b> RANJA was made in assignment from Dropstuff NL, with the purpose to be a 25 second clip, to be shown on tv-screens on train stations around the Netherlands. </b>
It was based on my poem, ‘RANJA’:  <br> <br>
Je half leeggedronken mok aanmaak-ijsthee balanceert op het randje van mijn vensterbank. Ik, met ongeschoren benen, in gammele staat, op het randje van mijn bed.  <br> <br>
Soms is het alsof ik de wereld vanuit het water zie.  <br> <br>
Wanneer ik vroeger met mijn ouders naar het zwembad ging, zwom ik meer ónder dan boven water. Dan keek ik naar de tenen van een onbekende man in een veel te strakke speedo-zwembroek, zocht ik naar vergeten speelgoed en vond ik ronddrijvende betadinepleisters in het chemisch gereinigde zwembadwater. Keek ik uit voor zelfverzonnen zeemonsters. Van onder het water klonk de wereld als een pas aangestoken waterpijp. Verdwenen gezichten en daarmee alle duidelijkheid in een wereld, bedoeld alleen voor mij. Wanneer ik thuiskwam met rode chloor-ogen betekende dit slechts dat ik in het zwembad was geweest. <br> <br>
Nu betekenen rode ogen wat anders. Is er niemand om voor thuis te komen. Normaal drink je altijd je glas in een keer leeg. <br> <br>
Het erge aan het hebben van een trauma, is dat je jezelf vervolgens je hele leven gewaarschuwd moet hebben. Ik vertel mezelf dat elke keer, als je weggaat, ik je morgen niet meer terug zien zal. <br> <br>
Je zegt me wel eens, dat ik je maar gewoon vertrouwen moet. Maar van onder het water klinkt alles zo vaag. En ik zie alleen je tenen en zelfverzonnen zeemonsters.  <br> <br>
